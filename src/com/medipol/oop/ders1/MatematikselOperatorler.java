package com.medipol.oop.ders1;

public class MatematikselOperatorler {

    public static void main(String[] args) {
//        + (Toplama)
//        - (Çıkarma)
//        * (Çarpma)
//        / (Bölme)
//        % (Mod)

        int a = 30;
        int b = a + 5;

        System.out.println(b);

        int c = 10 + 15;
        System.out.println(c);


        float d = 123.56f;
        int e = 10;

        float f = d - e;
        System.out.println(f);


        float g = 12.56f;
        float h = g * 3;
        System.out.println(h);

        System.out.println(3 + 5);

        System.out.println(5 / 2f);
        System.out.println(5f / 2);
        System.out.println(5 / 2.);
        System.out.println(5. / 2);

        float j = 14.5f;
        float k = (j + 2) / 3.f; // j değişkeninin iki fazlasının 3 ile bölümünü k değişkenine ata
        System.out.println(k);

        System.out.println(10 % 5);


    }
}





